<script lang="ts">
import { defineComponent } from "vue";
import { mapActions, mapState } from "pinia";
import { useProjectsStore } from "./store/projects";
import { projects } from "./db/projects";
import { ProjectInfo } from "./types/projects";
import InfoCard from "./components/InfoCard.vue";
import Tooltip from "./components/shared/Tooltip.vue";

export default defineComponent({
  name: "App",
  data() {
    return {
      project: {} as ProjectInfo,
      showTooltip: false,
      tooltip: {
        name: "" as string | undefined,
        category: "" as string | undefined,
        x: 0,
        y: 0,
      },
    };
  },
  components: {
    InfoCard,
    Tooltip,
  },
  created() {
    this.fetchProjects(projects);
    this.setProject(1);
  },
  methods: {
    handleClick(target: HTMLElement) {
      // console.log(target.parentElement?.classList[0]);
      if (
        target.parentElement != null &&
        target.parentElement.classList[0] === "leaf" &&
        target.parentElement.dataset.id
      )
        this.setProject(+target.parentElement.dataset.id);
    },
    getStatusColor(status: string): string {
      switch (status) {
        case "good":
          return "text-green-600";
        case "bad":
          return "text-red-600";
        case "ok":
          return "text-yellow-600";
        default:
          return "text-yellow-600";
      }
    },
    ...mapActions(useProjectsStore, ["fetchProjects"]),
    setProject(id: number) {
      let foundProject: ProjectInfo | undefined = projects.find(
        (item) => item.id === id
      );

      if (foundProject) this.project = { ...foundProject };
    },
    handlerTooltipShow(target: HTMLElement) {
      if (
        target.parentElement != null &&
        target.parentElement.classList[0] === "leaf"
      ) {
        this.showTooltip = true;
        this.tooltip = {
          category: target.parentElement.dataset.category,
          name: target.parentElement.dataset.name,
          x: target.parentElement.getBoundingClientRect().left,
          y: target.parentElement.getBoundingClientRect().top,
        };
      } else {
        this.tooltip = {
          category: "",
          name: "",
          x: 0,
          y: 0,
        };
        this.showTooltip = false;
      }
    },
  },
  computed: {
    ...mapState(useProjectsStore, ["getProjects"]),
    isShowTooltip() {
      return this.showTooltip;
    },
  },
});
</script>

